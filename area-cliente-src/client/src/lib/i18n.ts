export function getTranslation(lang: Lang, key: string): string {
  return translations[lang]?.[key] || key;
}

export function getStoredLang(): Lang {
  if (typeof window === 'undefined') return 'pt';
  // Allow ?lang=XX URL param to override stored preference (used by external links)
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get('lang');
  if (urlLang === 'pt' || urlLang === 'en' || urlLang === 'es') {
    localStorage.setItem('s2i-lang', urlLang);
    return urlLang as Lang;
  }
  const stored = localStorage.getItem('s2i-lang');
  if (stored === 'pt' || stored === 'en' || stored === 'es') return stored as Lang;
  // Default to PT for Share2Inspire (Portuguese-first product)
  return 'pt';
}

export function setStoredLang(lang: Lang) {
  localStorage.setItem('s2i-lang', lang);
}

export type I18nPickInput = { pt: string; en: string; es: string };

export type I18nContextType = {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string) => string;
  pick: (ptOrMap: string | I18nPickInput, en?: string, es?: string) => string;
};

export const I18nContext = createContext<I18nContextType>({
  lang: 'pt',
  setLang: () => {},
  t: (key: string) => key,
  pick: (ptOrMap: string | I18nPickInput, en?: string, es?: string) => {
    if (typeof ptOrMap === 'string') {
      return ptOrMap;
    }
    return ptOrMap.pt;
  },
});

export function useI18n() {
  return useContext(I18nContext);
}
